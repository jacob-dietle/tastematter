//! Type definitions for intelligence service integration
//!
//! These types match the TypeScript intelligence service API at localhost:3002.
//! CRITICAL: Use `#[serde(rename_all = "kebab-case")]` for ChainCategory to match TypeScript.

use chrono::{DateTime, Utc};
use serde::{Deserialize, Serialize};
use std::collections::HashMap;

/// Work category for chain naming - matches TypeScript ChainCategorySchema
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "kebab-case")]
pub enum ChainCategory {
    BugFix,
    Feature,
    Refactor,
    Research,
    Cleanup,
    Documentation,
    Testing,
    Unknown,
}

/// Request to name a conversation chain
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ChainNamingRequest {
    pub chain_id: String,
    pub files_touched: Vec<String>,
    pub session_count: i32,
    pub recent_sessions: Vec<String>,

    // Enrichment fields (all Optional for backward compat)
    /// Aggregated tool usage across sessions: {"Read": 23, "Edit": 12}
    #[serde(skip_serializing_if = "Option::is_none")]
    pub tools_used: Option<HashMap<String, u32>>,
    /// User's stated intent from first message in chain
    #[serde(skip_serializing_if = "Option::is_none")]
    pub first_user_intent: Option<String>,
    /// Commit messages from the chain: ["feat: Add query engine"]
    #[serde(skip_serializing_if = "Option::is_none")]
    pub commit_messages: Option<Vec<String>>,

    // A/B test fields - explicit separation for quality comparison
    /// First user message only (for A/B testing naming quality)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub first_user_message: Option<String>,
    /// Full conversation excerpt - all user messages (~8K chars)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub conversation_excerpt: Option<String>,
}

/// Response from chain naming endpoint
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ChainNamingResponse {
    pub chain_id: String,
    pub generated_name: String,
    pub category: ChainCategory,
    pub confidence: f32,
    pub model_used: String,
}

/// Cached chain metadata in SQLite
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ChainMetadata {
    pub chain_id: String,
    pub generated_name: Option<String>,
    pub category: Option<ChainCategory>,
    pub confidence: Option<f32>,
    pub generated_at: Option<DateTime<Utc>>,
    pub model_used: Option<String>,
}

