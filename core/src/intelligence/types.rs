/// Work status for chain summary - matches TypeScript WorkStatusSchema
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum WorkStatus {
    InProgress,
    Complete,
    Paused,
    Abandoned,
}

/// Source of a workstream tag - existing from config or generated by model
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "lowercase")]
pub enum WorkstreamTagSource {
    Existing,
    Generated,
}

/// A workstream tag with its source tracked
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct WorkstreamTag {
    pub tag: String,
    pub source: WorkstreamTagSource,
}

/// Request to summarize a conversation chain with workstream tagging
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ChainSummaryRequest {
    pub chain_id: String,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub conversation_excerpt: Option<String>,
    pub files_touched: Vec<String>,
    pub session_count: i32,
    #[serde(skip_serializing_if = "Option::is_none")]
    pub duration_seconds: Option<i64>,
    /// Existing workstreams from workstreams.yaml for hybrid tagging
    #[serde(skip_serializing_if = "Option::is_none")]
    pub existing_workstreams: Option<Vec<String>>,
}

/// Response from chain summary endpoint
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ChainSummaryResponse {
    pub chain_id: String,
    pub summary: String,
    pub accomplishments: Vec<String>,
    pub status: WorkStatus,
    pub key_files: Vec<String>,
    pub workstream_tags: Vec<WorkstreamTag>,
    pub model_used: String,
}

