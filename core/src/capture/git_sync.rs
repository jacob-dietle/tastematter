//! Git commit sync to SQLite database.
//!
//! Syncs git log data to SQLite for unified querying with file events and Claude sessions.
//!
//! Port of Python `capture/git_sync.py`.

use chrono::{DateTime, Utc};
use serde::{Deserialize, Serialize};

// ============================================================================
// Type Definitions
// ============================================================================

/// Parsed git commit with all metadata.
/// Maps 1:1 to Python GitCommit dataclass.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct GitCommit {
    /// Full 40-char hash
    pub hash: String,
    /// First 7 chars
    pub short_hash: String,
    /// Author date (ISO8601)
    pub timestamp: DateTime<Utc>,
    /// Author name
    pub author_name: String,
    /// Author email
    pub author_email: String,
    /// Commit message (first line / subject)
    pub message: String,
    /// All files changed (A/M/D combined)
    pub files_changed: Vec<String>,
    /// Files with 'A' status
    pub files_added: Vec<String>,
    /// Files with 'M' status
    pub files_modified: Vec<String>,
    /// Files with 'D' status
    pub files_deleted: Vec<String>,
    /// Lines added
    pub insertions: i32,
    /// Lines removed
    pub deletions: i32,
    /// Total files changed count
    pub files_count: i32,
    /// True if commit contains Claude Code signature
    pub is_agent_commit: bool,
    /// True if commit has multiple parents
    pub is_merge_commit: bool,
}

/// Options for git sync operation.
#[derive(Debug, Clone, Default, Serialize, Deserialize)]
pub struct SyncOptions {
    /// Time range: "90 days", "2025-01-01", etc.
    #[serde(skip_serializing_if = "Option::is_none")]
    pub since: Option<String>,
    /// Upper bound date
    #[serde(skip_serializing_if = "Option::is_none")]
    pub until: Option<String>,
    /// Path to git repository (default: ".")
    #[serde(skip_serializing_if = "Option::is_none")]
    pub repo_path: Option<String>,
    /// Only sync new commits (default: true)
    #[serde(default = "default_true")]
    pub incremental: bool,
}

fn default_true() -> bool {
    true
}

/// Result of sync operation.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct SyncResult {
    /// Number of commits successfully synced
    pub commits_synced: i32,
    /// Number of commits skipped (already in DB)
    pub commits_skipped: i32,
    /// Hash of most recent commit synced
    pub last_hash: String,
    /// Non-fatal parse errors
    pub errors: Vec<String>,
}

// ============================================================================
// Agent Commit Detection
// ============================================================================

/// Agent commit detection signatures.
/// Patterns that indicate a commit was generated by Claude Code.
pub const AGENT_SIGNATURES: &[&str] = &[
    "generated with claude code",
    "🤖 generated with",
    "co-authored-by: claude",
];

/// Detect if commit message contains Claude Code signature.
///
/// Checks commit message for known signatures (case-insensitive).
///
/// # Arguments
/// * `message` - Commit message (can be multi-line)
///
/// # Returns
/// `true` if commit appears to be agent-generated
pub fn detect_agent_commit(message: &str) -> bool {
    let message_lower = message.to_lowercase();
    AGENT_SIGNATURES
        .iter()
        .any(|sig| message_lower.contains(sig))
}

// ============================================================================
// Commit Parsing
// ============================================================================

/// Parse error for git log output.
#[derive(Debug, Clone)]
pub struct ParseError {
    pub message: String,
}

impl std::fmt::Display for ParseError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "ParseError: {}", self.message)
    }
}

impl std::error::Error for ParseError {}

/// Parse a single commit block from git log output.
///
/// A block consists of:
/// 1. Format line (hash§short§date§name§email§subject§parents)
/// 2. Empty line (optional)
/// 3. Numstat lines (insertions\tdeletions\tfile)
/// 4. Empty line (optional)
/// 5. Name-status lines (M\tfile or A\tfile or D\tfile)
///
/// # Arguments
/// * `block` - Raw text block for one commit
///
/// # Returns
/// `GitCommit` with all fields populated
pub fn parse_commit_block(block: &str) -> Result<GitCommit, ParseError> {
    let lines: Vec<&str> = block.trim().lines().collect();

    if lines.is_empty() {
        return Err(ParseError {
            message: "Empty commit block".to_string(),
        });
    }

    // Line 1: Format string
    let format_line = lines[0];
    let parts: Vec<&str> = format_line.split('§').collect();

    if parts.len() != 7 {
        return Err(ParseError {
            message: format!(
                "Expected 7 parts in format line, got {}: {}",
                parts.len(),
                &format_line[..format_line.len().min(100)]
            ),
        });
    }

    let hash_full = parts[0].to_string();
    let hash_short = parts[1].to_string();
    let timestamp_str = parts[2];
    let name = parts[3].to_string();
    let email = parts[4].to_string();
    let subject = parts[5].to_string();
    let parents = parts[6];

    // Parse timestamp
    let timestamp = chrono::DateTime::parse_from_rfc3339(timestamp_str)
        .map(|dt| dt.with_timezone(&Utc))
        .unwrap_or_else(|_| Utc::now());

    // Detect merge commit (multiple parents)
    let parent_list: Vec<&str> = parents.split_whitespace().collect();
    let is_merge = parent_list.len() > 1;

    // Parse numstat and name-status lines
    let mut insertions = 0;
    let mut deletions = 0;
    let mut files_added: Vec<String> = Vec::new();
    let mut files_modified: Vec<String> = Vec::new();
    let mut files_deleted: Vec<String> = Vec::new();

    for line in lines.iter().skip(1) {
        let line = line.trim();
        if line.is_empty() {
            continue;
        }

        if line.contains('\t') {
            let parts: Vec<&str> = line.split('\t').collect();

            // Detect line type based on content
            // Numstat format: <number>\t<number>\t<filepath> (or -\t-\t for binary)
            // Name-status format: <single-letter>\t<filepath>

            if parts.len() >= 3 {
                let ins_str = parts[0];
                let del_str = parts[1];

                // Check if this looks like numstat (numbers or -)
                let is_numstat = (ins_str == "-" || ins_str.parse::<i32>().is_ok())
                    && (del_str == "-" || del_str.parse::<i32>().is_ok());

                if is_numstat {
                    // Handle binary files (show - instead of numbers)
                    if ins_str != "-" {
                        if let Ok(n) = ins_str.parse::<i32>() {
                            insertions += n;
                        }
                    }
                    if del_str != "-" {
                        if let Ok(n) = del_str.parse::<i32>() {
                            deletions += n;
                        }
                    }
                    continue;
                }
            }

            // Name-status format: status<tab>filepath (or status<tab>oldpath<tab>newpath for renames)
            if parts.len() >= 2 {
                let status = parts[0];
                // Verify it's a status letter
                if status.len() == 1 && "AMDRC".contains(status) {
                    let filepath = parts.last().unwrap().to_string(); // Use last part (handles renames)

                    match status {
                        "A" => files_added.push(filepath),
                        "M" => files_modified.push(filepath),
                        "D" => files_deleted.push(filepath),
                        "R" => files_modified.push(filepath), // Rename: treat as modify of new file
                        _ => {}
                    }
                }
            }
        }
    }

    let mut all_files = Vec::new();
    all_files.extend(files_added.iter().cloned());
    all_files.extend(files_modified.iter().cloned());
    all_files.extend(files_deleted.iter().cloned());

    // Detect agent commit
    let is_agent = detect_agent_commit(&subject);

    Ok(GitCommit {
        hash: hash_full,
        short_hash: hash_short,
        timestamp,
        author_name: name,
        author_email: email,
        message: subject,
        files_changed: all_files.clone(),
        files_added,
        files_modified,
        files_deleted,
        insertions,
        deletions,
        files_count: all_files.len() as i32,
        is_agent_commit: is_agent,
        is_merge_commit: is_merge,
    })
}

/// Split raw git log output into individual commit blocks.
///
/// Commits are separated by the format line pattern (starts with 40-char hash).
///
/// # Arguments
/// * `raw_output` - Raw output from git log command
///
/// # Returns
/// List of commit block strings
pub fn split_commit_blocks(raw_output: &str) -> Vec<String> {
    let mut blocks = Vec::new();
    let mut current_block_lines: Vec<&str> = Vec::new();

    for line in raw_output.lines() {
        // Detect start of new commit (40-char hash followed by §)
        let parts: Vec<&str> = line.split('§').collect();
        let is_new_commit = parts.len() > 1
            && parts[0].len() == 40
            && parts[0].chars().all(|c| c.is_ascii_hexdigit());

        if is_new_commit {
            // Save previous block
            if !current_block_lines.is_empty() {
                blocks.push(current_block_lines.join("\n"));
            }
            current_block_lines = vec![line];
        } else {
            current_block_lines.push(line);
        }
    }

    // Don't forget last block
    if !current_block_lines.is_empty() {
        blocks.push(current_block_lines.join("\n"));
    }

    blocks
}

