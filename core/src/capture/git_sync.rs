use std::path::Path;
use std::process::Command;

/// Error running git command.
#[derive(Debug, Clone)]
pub struct GitError {
    pub message: String,
}

impl std::fmt::Display for GitError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "GitError: {}", self.message)
    }
}

impl std::error::Error for GitError {}

/// Fetch full commit message body for a single commit.
///
/// Used for agent detection which needs to check the full body.
pub fn get_commit_body(commit_hash: &str, repo_path: &Path) -> String {
    let result = Command::new("git")
        .args(["log", "-1", "--format=%B", commit_hash])
        .current_dir(repo_path)
        .output();

    match result {
        Ok(output) if output.status.success() => {
            String::from_utf8_lossy(&output.stdout).to_string()
        }
        _ => String::new(),
    }
}

/// Sync git commits from repository.
///
/// Runs `git log` and parses commits. Does NOT write to database.
/// Caller is responsible for database operations.
///
/// # Arguments
/// * `options` - Sync configuration (since, until, repo_path)
///
/// # Returns
/// * List of parsed commits
pub fn sync_commits(options: &SyncOptions) -> Result<(Vec<GitCommit>, SyncResult), GitError> {
    let repo_path = options.repo_path.as_deref().unwrap_or(".");

    // Build git log command
    // Format: hash§short§date§name§email§subject§parents
    let mut cmd = Command::new("git");
    cmd.args([
        "log",
        "--format=%H§%h§%aI§%an§%ae§%s§%P",
        "--numstat",
        "--name-status",
    ]);

    if let Some(ref since) = options.since {
        cmd.args(["--since", since]);
    }

    if let Some(ref until) = options.until {
        cmd.args(["--until", until]);
    }

    cmd.current_dir(repo_path);

    // Execute git log
    let output = cmd.output().map_err(|e| GitError {
        message: format!("git command failed: {}", e),
    })?;

    if !output.status.success() {
        let stderr = String::from_utf8_lossy(&output.stderr);
        return Err(GitError {
            message: format!("git log failed: {}", stderr),
        });
    }

    let raw_output = String::from_utf8_lossy(&output.stdout);

    // Split into commit blocks
    let blocks = split_commit_blocks(&raw_output);

    // Parse commits
    let mut commits = Vec::new();
    let mut errors = Vec::new();
    let mut newest_hash = String::new();

    for block in blocks {
        if block.trim().is_empty() {
            continue;
        }

        match parse_commit_block(&block) {
            Ok(mut commit) => {
                // Check full body for agent detection (subject only isn't enough)
                if !commit.is_agent_commit {
                    let full_body = get_commit_body(&commit.hash, Path::new(repo_path));
                    if detect_agent_commit(&full_body) {
                        commit.is_agent_commit = true;
                    }
                }

                if newest_hash.is_empty() {
                    newest_hash.clone_from(&commit.hash);
                }
                commits.push(commit);
            }
            Err(e) => {
                errors.push(e.message);
            }
        }
    }

    let result = SyncResult {
        commits_synced: commits.len() as i32,
        commits_skipped: 0, // Will be updated by caller after DB check
        last_hash: newest_hash,
        errors,
    };

    Ok((commits, result))
}

